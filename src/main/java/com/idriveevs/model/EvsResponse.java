public class EvsResponse<T> extends ModelBase {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6033926424123872900L;
	
	private int status;
	
	private T response;
	


	/**
	 * @return the response
	 */
	public T getResponse() {
		return response;
	}

	/**
	 * @param response the response to set
	 */
	public void setResponse(T response) {
		this.response = response;
	}

	/**
	 * @return the status
	 */
	public int getStatus() {
		return status;
	}

	/**
	 * @param status the status to set
	 */
	public void setStatus(int status) {
		this.status = status;
	}

	
	
	
	
}
